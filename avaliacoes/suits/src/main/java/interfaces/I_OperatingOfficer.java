package interfaces;

/**
 * Interface I_OperatingOfficer que define os métodos que um oficial de operações deve implementar.
 */
public interface I_OperatingOfficer {
    
    /**
     * Método para apoiar executivos.
     */
    void supportExecutives();

    /**
     * Método para desenvolver políticas.
     */
    void developPolicies();

    /**
     * Método para coordenar atividades.
     */
    void coordinate();
    
}
